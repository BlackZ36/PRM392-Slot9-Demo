package com.example.prm392_slot9_demo.model;

public class Value {
    private String value;
    private String imgUrl;

    // Constructor, getters and setters
    // Constructor, getters and setters
    public Value(String value, String imgUrl) {
        this.value = value;
        this.imgUrl = imgUrl;
    }

    public String getValue() {
        return value;
    }

    public String getImgUrl() {
        return imgUrl;
    }
}
